def Calcular_Perimetro_Cuadrado ():
    Lado = int(input("Ingrese un lado del Cuadrado: "))
    Perimetro = Lado * 4
    print("El perimetro del Cuadrado es:" , Perimetro)

def Calcular_Perimetro_Rectangulo ():
    Base = int(input("Ingrese la Base del Rectangulo: "))
    Altura = int(input("Ingrese la altura del Rectangulo: "))
    Perimetro = (Base * 2) + (Altura * 2)
    print("El perimetro del Rectangulo es:" , Perimetro)

def Calcular_Perimetro_Triangulo ():
    Lado = int(input("Ingrese lado del Triangulo: "))
    Perimetro = Lado * 3
    print("El perimetro del Triangulo es:" , Perimetro)

def Calcular_Perimetro_Rombo ():
    Lado = int(input("Ingrese lado del Rombo: "))
    Perimetro = Lado * 4
    print("El perimetro del Rombo es:" , Perimetro)